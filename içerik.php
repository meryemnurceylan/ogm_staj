​​Ülkemizde ve  Dünya da büyük bir önem arz eden doğal zenginliklerin korunması ve biyolojik çeşitliliğe doğal yaşama dönük bilgilerin toplum ile paylaşımı büyük önem taşımaktadır. Bu doğrultuda Antalya Orman Bölge Müdürlüğü bünyesinde , Uluslararası Ormancılık Eğitim Merkezi Müdürlüğü  içerisinde bulunan  Biyolojik Mücadele Laboratuvarında ziyarete açılan böcek koleksiyonu ilgi çekiyor. Koleksiyonda; Türkiye'deki ormanlarda bulunan böceklerden Coleoptera (Örtük Kanatlılar), Lepidoptera (Pul Kanatlılar-Kelebekler), Orthoptera (Düz Kanatlılar), Hymenoptera (Zar Kanatlılar), Homoptera (Benzer Kanatlılar), Odonota (Yusufçuklar) takımlarına ait türler yer almaktadır.

